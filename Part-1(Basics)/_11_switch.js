const day = 'sunday';

switch (day) {
    case 'saturday':
        console.log("I do a lot coding!");
        break;
    case 'sunday':
        console.log("I watch movies and fun with friends!");
        break;
    case 'monday':
    case 'tuesday':
        console.log("Work on both monday and tuesday");
        break;
    default:
        console.log("Not a valid day!");

};

